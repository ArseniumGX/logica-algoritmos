/* 
    Sabendo que a área quadrada é dada pela multiplicação dos lados, escreva 
    um algoritmo que mostre a área quadrada de um espaço qualquer.

    Exemplo:

    - A área de 3x9 é igual a 27
    - 3 . 9 = 27

*/

function area(altura, largura){
    return altura * largura
}

module.exports = area